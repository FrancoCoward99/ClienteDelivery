    private void llenarDatos() {
        txtNombreConfirmar.setText(cliente.getNombre());
        txtTelefonoConfirmar.setText(String.valueOf(cliente.getNumeroTelefono()));
        txtDireccionConfirmar.setText(cliente.getDireccion());
        txtRestauranteConfirmar.setText(restaurante);
        
        StringBuilder detallesPedido = new StringBuilder();
        for (Producto producto : productos) {
            detallesPedido.append(producto.getNombre()).append(" - ").append(producto.getPrecio()).append("\n");
        }
        txtPedidoConfirmar.setText(detallesPedido.toString());
    }

