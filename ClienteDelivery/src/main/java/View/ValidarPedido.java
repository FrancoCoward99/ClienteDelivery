    private ArrayList<Producto> parsearProductos(String textoProductos) {
        ArrayList<Producto> productos = new ArrayList<>();
        String[] lineas = textoProductos.split("\n");
        for (String linea : lineas) {
            String[] partes = linea.split(" - "); // Asumiendo que el formato es "Nombre - Precio"
            if (partes.length == 2) {
                String nombre = partes[0];
                int precio = Integer.parseInt(partes[1].replaceAll("[^\\d]", "")); // Elimina cualquier carácter no numérico
                productos.add(new Producto(nombre, precio));
            }
        }
        return productos;
    }
    
