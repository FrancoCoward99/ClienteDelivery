    private void btnConfirmarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarPedidoActionPerformed
        // TODO add your handling code here:
        try {
            // Recuperar datos del formulario
            String nombre = txtNombreConfirmar.getText();
            int telefono = Integer.parseInt(txtTelefonoConfirmar.getText());
            String direccion = txtDireccionConfirmar.getText();
            Cliente cliente = new Cliente(nombre, telefono, direccion);

            // Crear lista de productos desde el resumen de pedido
            ArrayList<Producto> productos = parsearProductos(txtPedidoConfirmar.getText());

            // Usar el método existente en el controlador para enviar el pedido
             Pedido elPedidoCreado = new Pedido(new Random().nextInt(100000) + 1, cliente, new Restaurante(txtRestauranteConfirmar.getText(), productos));
                controlador.enviarPedido(elPedidoCreado);


            // Mensaje de confirmación y cierre de ventana
            JOptionPane.showMessageDialog(this, "Pedido confirmado y enviado al servidor.");
            this.dispose(); // Cierra la ventana después de enviar el pedido
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, verifique los datos. El número de teléfono debe ser válido.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al procesar el pedido: " + e.getMessage());
        }
    }//GEN-LAST:event_btnConfirmarPedidoActionPerformed

